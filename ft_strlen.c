int	ft_strlen(char *str)
{
	int	cont;
	int	i;

	i = 0;
	if (str[i] == '\0')
		return (0);
	cont = 0;
	while (str[i] != '\0')
	{
		cont = cont + 1;
		i++;
	}
	return (cont);
}
